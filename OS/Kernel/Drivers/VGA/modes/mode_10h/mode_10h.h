unsigned char *MODE10H_BUFFER[] = {NULL, NULL, NULL, NULL};
unsigned char bufferTurnedOn10H = 0;

int8_t setMode10H()
{
    writeRegisters(g_640x480x16);
    set_vga_palette(palette10H);
    return 0x13;
}

int8_t turnOnBuffer10H()
{
    if(bufferTurnedOn10H) return -1;
    for(int i = 3; i >= 0; i--)
    {
        MODE10H_BUFFER[i] = heap_kernel_alloc(MODE10H_HEIGHT * MODE10H_WIDTH / 8, 0);
        if(MODE10H_BUFFER[i] == NULL)
        {
            for(int j = 3; j >= i; j--)
            {
                heap_kernel_dealloc(MODE10H_BUFFER[j]);
                MODE10H_BUFFER[j] = NULL;
            }
            return -1;
        }
    }
    setDrawPixelFunc(&drawPixel10HBuffered);
    setDrawLineFunc(&drawLine10HBuffered);
    setDrawCircleFunc(&drawCircle10HBuffered);
    setDrawRectangleFunc(&drawRectangle10HBuffered);
    bufferTurnedOn10H = 1;
    return 0;
}

int8_t turnOffBuffer10H()
{
    if(!bufferTurnedOn10H) return -1;
    for(int i = 3; i >= 0; i--)
    {
        heap_kernel_dealloc(MODE10H_BUFFER[i]);
        MODE10H_BUFFER[i] = NULL;
    }
    setDrawPixelFunc(&drawPixel10H);
    setDrawLineFunc(&drawLine10H);
    setDrawCircleFunc(&drawCircle10H);
    setDrawRectangleFunc(&drawRectangle10H);
    bufferTurnedOn10H = 0;
    return 0;
}

uint8_t isBufferOn10H()
{
    return bufferTurnedOn10H;
}

int8_t swapBuffers10H()
{
    if(!bufferTurnedOn10H) return -1;
    for(char p = 3; p >= 0; p--)
    {
        set_plane(p);
        memcpy(VGA_VRAM, MODE10H_BUFFER[p], MODE10H_WIDTH * MODE10H_HEIGHT / 8);
    }
    //memcpy(VGA_VRAM, MODE13H_BUFFER, MODE13H_HEIGHT * MODE13H_WIDTH);
    return 0;
}

int8_t drawPixel10H(uint8_t color, uint16_t x, uint16_t y)
{
    if((x>=MODE10H_WIDTH) || (y >=MODE10H_HEIGHT))
        return -1;
    unsigned char *fb = (unsigned char *) VGA_VRAM;
    unsigned int offset = (y * 640 + x)/8;
	unsigned bit_no = x % 8;
	for(char p = 3; p >= 0; p--)
	{
		set_plane(p);
		bit_write(fb[offset], 1<<(7-bit_no), (bit_get(color, 1 << p)));
	}
    return 0;
}

